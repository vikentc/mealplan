export function getTranslatedRecipe(recipe: any, language: string): any {
  if (!recipe) return recipe;
  if (language === 'en' && recipeTranslations[recipe.id]) {
    const trans = recipeTranslations[recipe.id];
    return {
      ...recipe,
      name: trans.name,
      description: trans.description || recipe.description,
      ingredients: recipe.ingredients.map((ing: any, idx: number) => ({
        ...ing,
        name: trans.ingredients[idx] || ing.name
      })),
      instructions: trans.instructions || recipe.instructions
    };
  }
  return recipe;
}
